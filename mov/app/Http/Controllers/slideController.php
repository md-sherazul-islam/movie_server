        {
          $slides = Slide::create([
              'name'    => $request->input('name'),
              'image'   => $request->file('image')->getClientOriginalName(),
              'movie'   => $request->file('movie')->getClientOriginalName(),
          ]);
          if($slides)
          {
            return back()->with('success','Slide Show Updated Successfully');
